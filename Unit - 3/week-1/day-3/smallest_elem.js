function runProgram(input) {
	input = input.split(/[\r\n]+/);
	let arr = input[1].trim().split(' ').map(Number);

	let outputstr = '';

	for (let i = 0; i < arr.length; i++) {
		let j;
		for (j = i - 1; j >= 0; j--) {
			if (arr[j] < arr[i]) {
				outputstr += arr[j] + ' ';
				break;
			}
		}
		if (j === -1) outputstr += '-1 ';
	}
	console.log(outputstr);
}
